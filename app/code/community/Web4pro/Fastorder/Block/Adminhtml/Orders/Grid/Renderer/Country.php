class Web4pro_Fastorder_Block_Adminhtml_Orders_Grid_Renderer_Country extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Abstract
{
    /**
     * @param Varien_Object $row
     * @return string
     */
    public function render(Varien_Object $row)
    {
        return Mage::app()->getLocale()->getCountryTranslation($row->getCountry());
    }
}